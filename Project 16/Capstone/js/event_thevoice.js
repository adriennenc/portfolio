function like_ed1() {
    var image1 = document.getElementById("liked_like1");
    if (image1.src.match("images/like.jpg")) 
	if(typeof(Storage) !== "undefined") {
        if (localStorage.clickcount_01) {
            localStorage.clickcount_01 = Number(localStorage.clickcount_01)+1;
        } else {
            localStorage.clickcount_01 = 1;
        }
        document.getElementById("result1").innerHTML = "(" + localStorage.clickcount_01 + ")";
    } {
        image1.src = "images/liked.jpg";	
    }
}
